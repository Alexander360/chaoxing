package pers.cz.chaoxing.common;

public class PlayerDataProperty {
    private String jobid;
    private boolean switchwindow;
    private long size;
    private boolean fastforward;
    private String hsize;
    private String module;
    private String name;
    private String mid;
    private String type;
    private String objectid;
    private String _jobid;

    public String getJobid() {
        return jobid;
    }

    public void setJobid(String jobid) {
        this.jobid = jobid;
    }

    public boolean isSwitchwindow() {
        return switchwindow;
    }

    public void setSwitchwindow(boolean switchwindow) {
        this.switchwindow = switchwindow;
    }

    public long getSize() {
        return size;
    }

    public void setSize(long size) {
        this.size = size;
    }

    public boolean isFastforward() {
        return fastforward;
    }

    public void setFastforward(boolean fastforward) {
        this.fastforward = fastforward;
    }

    public String getHsize() {
        return hsize;
    }

    public void setHsize(String hsize) {
        this.hsize = hsize;
    }

    public String getModule() {
        return module;
    }

    public void setModule(String module) {
        this.module = module;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMid() {
        return mid;
    }

    public void setMid(String mid) {
        this.mid = mid;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getObjectid() {
        return objectid;
    }

    public void setObjectid(String objectid) {
        this.objectid = objectid;
    }

    public String get_jobid() {
        return _jobid;
    }

    public void set_jobid(String _jobid) {
        this._jobid = _jobid;
    }
}
